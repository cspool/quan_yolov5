class MobileCBR(nn.Sequential):
    def __init__(self,in_channel,out_channel,kernel_size=3,stride=1,groups=1):
        padding = (kernel_size - 1) // 2
        super(MobileCBR,self).__init__(
            nn.Conv2d(in_channel,out_channel,kernel_size,stride,padding,bias=False,groups=groups),
            nn.BatchNorm2d(out_channel),
            nn.ReLU6(inplace=True)
        )
    def fuseforward(self, input):
        for module in self:
            if module is None or type(module) is nn.BatchNorm2d:
                continue
            input = module(input)
        return input
    
class MobileResidual(nn.Module):
    def __init__(self,input,output,stride,expand_ratio):
        super(MobileResidual,self).__init__()
        self.stride = stride
        hidden_dim = int(round(input * expand_ratio))
        self.residual_en = self.stride == 1 and input == output
        layers = [] 
        if expand_ratio != 1:
            layers.append(MobileCBR(input,hidden_dim,kernel_size=1))
        layers.extend([
            MobileCBR(hidden_dim,hidden_dim,stride=stride,groups=hidden_dim),
            nn.Conv2d(hidden_dim,output,1,1,0,bias=False),
            nn.BatchNorm2d(output)    
        ])
        self.conv = nn.Sequential(*layers)
    def forward(self,x):
        if self.residual_en:
            return x + self.conv(x)
        else:
            return self.conv(x)
    
    def fuseforward(self, x):
        for i, module in enumerate(self.conv):
            if module is None or type(module) is nn.BatchNorm2d:
                del self.conv[i]
        if self.use_res_connect:
            return x + self.conv(x)
        else:
            return self.conv(x)

